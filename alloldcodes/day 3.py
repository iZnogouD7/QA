match day:
    case 1:
        print("This is first day")
    case 2:
        print("This is second day")
    case 3:
        print("This is third day")
    case 4:
        print("This is fourth day")
    case 5:
        print("This is fifth day")
    case _ :
        print("This is default case")
while (day<12):
    print(day)
    day +=1
else:
    print("While has been completed")
